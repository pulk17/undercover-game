// ── Seed runner ───────────────────────────────────────────────────────────────

const BATCH_SIZE = 100;

async function seed() {
  console.log(`\n🌱  Starting seed — ${pairs.length} word pairs\n`);

  let inserted = 0;
  let skipped  = 0;

  for (let i = 0; i < pairs.length; i += BATCH_SIZE) {
    const batch = pairs.slice(i, i + BATCH_SIZE);

    const { data, error } = await supabase
      .from('word_pairs')
      .insert(batch)
      .select('id');

    if (error) {
      // 23505 = unique_violation — already seeded, skip this batch
      if (error.code === '23505') {
        console.log(`  Batch ${Math.floor(i / BATCH_SIZE) + 1} — skipped (already seeded)`);
        skipped += batch.length;
      } else {
        console.error(`❌  Batch ${Math.floor(i / BATCH_SIZE) + 1} failed:`, error.message);
        process.exit(1);
      }
    } else {
      const batchInserted = data?.length ?? 0;
      const batchSkipped  = batch.length - batchInserted;
      inserted += batchInserted;
      skipped  += batchSkipped;

      const batchNum    = Math.floor(i / BATCH_SIZE) + 1;
      const totalBatches = Math.ceil(pairs.length / BATCH_SIZE);
      console.log(`  Batch ${batchNum}/${totalBatches} — inserted: ${batchInserted}, skipped: ${batchSkipped}`);
    }
  }

  console.log(`\n✅  Seed complete!`);
  console.log(`   Total processed : ${pairs.length}`);
  console.log(`   Newly inserted  : ${inserted}`);
  console.log(`   Already existed : ${skipped}`);

  const { count, error: countError } = await supabase
    .from('word_pairs')
    .select('*', { count: 'exact', head: true })
    .eq('is_custom', false);

  if (!countError) {
    console.log(`   Library total   : ${count} pairs in database\n`);
  }
}

seed().catch((err) => {
  console.error('Fatal error during seed:', err);
  process.exit(1);
});